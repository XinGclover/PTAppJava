package com.example.backend.entities;

public class Aero {
    private float airspeed;
    private float alpha;
    private float altitude;
    private float beta;
    private float density;
    private float mach;
    private float rate_P;
    private float rate_Q;
    private float rate_R;

    public float getAirspeed() {
        return airspeed;
    }

    public void setAirspeed(float airspeed) {
        this.airspeed = airspeed;
    }

    public float getAlfha() {
        return alpha;
    }

    public void setAlfha(float alfha) {
        this.alpha = alfha;
    }

    public float getAltitude() {
        return altitude;
    }

    public void setAltitude(float altitude) {
        this.altitude = altitude;
    }

    public float getBeta() {
        return beta;
    }

    public void setBeta(float beta) {
        this.beta = beta;
    }

    public float getDensity() {
        return density;
    }

    public void setDensity(float density) {
        this.density = density;
    }

    public float getMach() {
        return mach;
    }

    public void setMach(float mach) {
        this.mach = mach;
    }

    public float getRate_P() {
        return rate_P;
    }

    public void setRate_P(float rate_P) {
        this.rate_P = rate_P;
    }

    public float getRate_Q() {
        return rate_Q;
    }

    public void setRate_Q(float rate_Q) {
        this.rate_Q = rate_Q;
    }

    public float getRate_R() {
        return rate_R;
    }

    public void setRate_R(float rate_R) {
        this.rate_R = rate_R;
    }
}
